#include<stdio.h>

int main(){

    for(int i=0; i<=10; i++){
        printf("%d \n", i);
    }
    // i=i+1 = i+=1 = 1++
    // ++1 - Pre increment - Add 1st and then use
    // 1++ - Post increment - Use 1st then add


    // --1 - Pre decrement - Subtract 1st and then use
    // 1-- - Post decrement - Use 1st then subtract
    
    return 0;
} 